class Solution {
    public static ArrayList<ArrayList<Integer>> findPairsWithGivenSum(int target, Node head) {
        // code here
        Node tail=head;
        ArrayList<ArrayList<Integer>> arr=new ArrayList<ArrayList<Integer>>();
        while(tail.next!=null){
            tail=tail.next;
        }
        //first traverse to the last element.
        //now from last element anf first element compare them.
        //find their sum and check it to the target value.
        
        while(head!=tail){
            int sum=head.data+tail.data;
            //if sum= target store in the array list.
            if(sum==target){
                ArrayList<Integer> arr1=new ArrayList<Integer>();
                arr1.add(head.data);
                arr1.add(tail.data);
                arr.add(arr1);
                head=head.next;
            }
            // if sum greater than target,
            // then in order to reduce value go back from higher element to next higher and compare.
            else if(sum>target){
                tail=tail.prev;
            }
             //  if sum less than target then, to increase value go next from start.
            else{
                head=head.next;
            }
        }
        return arr;

    }
}
        
